const fs = require('fs');
const path = require('path');
const dotenv = require('dotenv');

const COMMENT = "This file is generated by `yarn run update:contracts`. Don't edit manually.";
const CONTRACTS_URL =
  'https://raw.githubusercontent.com/eq-lab/Slender/master/deploy/artifacts/mainnet/.contracts';
const OUTPUT = '../src/shared/stellar/constants/contracts.ts';

(async () => {
  const response = await fetch(CONTRACTS_URL);
  const text = await response.text();
  const parsed = dotenv.parse(text);
  const constants = Object.entries(parsed).reduce(
    (output, [key, value]) => `${output}export const ${key} = ${JSON.stringify(value)}\n`,
    `// ${COMMENT}
`,
  );
  fs.writeFileSync(path.join(__dirname, OUTPUT), constants);
})();
